package cn.xinzhili.api.doctor.bean;

public class BasicCoronaryStentApiInfo {

  private String id;
  private String company;
  private String name;
  private String material;
  private String thickness;
  private String coatingDrug;
  private String polymericCarrier;
  private String baseCoat;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getCompany() {
    return company;
  }

  public void setCompany(String company) {
    this.company = company;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getMaterial() {
    return material;
  }

  public void setMaterial(String material) {
    this.material = material;
  }

  public String getThickness() {
    return thickness;
  }

  public void setThickness(String thickness) {
    this.thickness = thickness;
  }

  public String getCoatingDrug() {
    return coatingDrug;
  }

  public void setCoatingDrug(String coatingDrug) {
    this.coatingDrug = coatingDrug;
  }

  public String getPolymericCarrier() {
    return polymericCarrier;
  }

  public void setPolymericCarrier(String polymericCarrier) {
    this.polymericCarrier = polymericCarrier;
  }

  public String getBaseCoat() {
    return baseCoat;
  }

  public void setBaseCoat(String baseCoat) {
    this.baseCoat = baseCoat;
  }
}
